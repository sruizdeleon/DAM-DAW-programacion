package clase241023;

public class ejercicio_00 {

	public static void main(String[] args) {
		/* 
		 * Ejercicio: comparar i++ y ++i
		 * Declara una variable i con valor 10
		 * Usa i++ y ++i en dos operaciones diferentes y muestra los resultados
		 * 
		 * 
		 */
		
		int i = 10;
		System.out.println("Valor de i es de: "+ i);
		System.out.println("Valor de i es de: "+ i++);
		System.out.println("Valor de i es de: "+ i);
		
		int j = 10;
		System.out.println("Valor de j es de: "+ j);
		System.out.println("Valor de j es de: "+ ++j);
		System.out.println("Valor de j es de: "+ j);

	}

}
